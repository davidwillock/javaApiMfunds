package com.charlie1.funds.model;




public class jRisk {
	
	
	String risk1;
	String risk2;
	String symid;
	
	
	
	public jRisk() {}
	
	public jRisk(String risk1, String risk2) {
		
		this.risk1 = risk1;
		this.risk2 = risk2;
		
	}
	
	
	
	public String getSymid() {
		return symid;
	}
	
	public void setSymid(String symid) {
		this.symid = symid;
	}
	
		

	public String getRisk1() {
		return risk1;
	}

	public void setRisk1(String risk1) {
		this.risk1 = risk1;
	}

	public String getRisk2() {
		return risk2;
	}

	public void setRisk2(String risk2) {
		this.risk2 = risk2;
	}
	
	
	
	
	
	
	

}
